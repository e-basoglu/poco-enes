/* Odd / even reporter 2.0
Write a for loop that will iterate from 0 to 20.
For each iteration, it will check if the current number is even or odd.
Report the result to the screen (e.g. "2 is even"). */


for (let i = 0; i <= 20; i++) {
    if (i % 2 === 0) {
        alert(`${i} is even`);
    } else {
        alert(`${i} is odd`);
    }
}




